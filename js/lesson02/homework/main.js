// - Скласти розклад на тиждень за домопоги switch. Користувач вводить порядковий номер дня тижня і на екрані відображається інфа що заплановано на цей день.
//
let day = +prompt("Введите номер дня недели");
switch (day) {
    case 1:
        console.log("Понедельник - отдыхаем после выходных");
        break;
    case 2:
        console.log("Вторник - готовимся к работе");
        break;
    case 3:
        console.log("Среда - работаем");
        break;
    case 4:
        console.log("Четверг - отдыхаем после работы");
        break;
    case 5:
        console.log("Пятница - готовимся к выходным");
        break;
    case 6:
        console.log("Суббота - отдыхаем");
        break;
    case 7:
        console.log("Воскресенье - отдыхаем");
        break;
    default:
        console.log("Неверные данные!");
}


